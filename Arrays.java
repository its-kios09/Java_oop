public class Arrays {
    public static void main(String[] args){
//        An array is a container object that holds a fixed number of values of a single type
//         Declaration and allocating  of an Array
//        (+) is used to concatenate
        int[] array = new int[10];
        array[0] = 1;
        array[1] = 2;
        array[2] = 4;
        array[3] = 10;
        array[4] = 23;

//        accessing the values using the index
        System.out.println("index 0 the value is "+ array[0]);
        System.out.println("index 1 the value is "+ array[1]);
        System.out.println("index 2 the value is "+ array[2]);
        System.out.println("index 3 the value is "+ array[3]);
        System.out.println("index 4 the value is "+ array[4]);

    }
}
