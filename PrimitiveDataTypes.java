public class PrimitiveDataTypes {
    public static void main(String[] args){
//        Primitive data types
        byte a = 1;
        String b = "D";
        short c = 33;
        int d = 45;
        float e = 1.2423233f;
        double f = 1.23342222d;
        boolean g = true;

        System.out.println("This is a byte:"+ a);
        System.out.println("This is a char:"+ b);
        System.out.println("This is a short:"+ c);
        System.out.println("This is a int:"+ d);
        System.out.println("This is a float:"+ e);
        System.out.println("This is a double:"+ f);
        System.out.println("This is a boolean:"+ g);
    }
}
